def sumar (x,y):
    suma = x+y
    return suma
print (sumar(2,4))

def restar (x,y):
    resta = x-y
    return resta
print (restar(4,2))

def multiplicar (x,y):
    multiplicacion = x*y
    return multiplicacion
print (multiplicar(4,2))

def dividir (x,y):
    divicion = x/y
    return divicion
print (dividir(4,2))

